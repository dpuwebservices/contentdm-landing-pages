                        <h1>About</h1>   

<p class="para">The Commonplace Book of Louis de Marillac is one of the more singular volumes in <a href=" http://cdm16106.contentdm.oclc.org/cdm/compoundobject/collection/p16106coll11/id/554/rec/1"> DePaul’s Vincentian Studies Collection</a>. It is a manuscript, meaning that it is written entirely by hand. It is also a commonplace book, a compilation of material quoted and paraphrased from other sources, and in Early Modern Europe a popular way to collect information found to be interesting or useful. Each commonplace book is different, giving insight into its particular owner.</p>
<p class="para">Louis de Marillac (1556-1604) was a member of the French aristocracy and the legal guardian of the eventual co-founder of the Daughters of Charity, St. Louise de Marillac. While scholars do not know the parentage of Louise—she was the illegitimate daughter of one of the male Marillacs—it is most likely that Louis was indeed her father as well as her guardian. The binding of Louis’s commonplace book dates from around 1600, suggesting that he would have presented it to Louise shortly before his death in 1604. Louise, who was born in 1591, was only a child at the time.</p>
<p class="para">The manuscript would have been originally written on unbound quires, or grouped sheets of folded paper, and later bound into a book. The decorative binding, in a style called “fanfare,” is attributed to famous French bookbinder Clovis Eve. It is stamped with the name “LOYSE. DE MARILLAC.” on its front, and on its rear features the motto “VNG DIEV. VNE FOY. VNG ROY.” or, “One God. One Faith. One King.”</p>
<p class="para">As the book is written entirely in a 16th century French hand, it is most accessible to scholars versed in paleography. Transcription and translation of sources such as this commonplace book are a manual process that cannot be automated. For more resources on French paleography, please visit the Newberry Library’s <a href="https://paleography.library.utoronto.ca/">French Renaissance Paleography website</a>.</p>
<p class="para">Louis’s commonplace book was once owned by famed book collector Carrie Estelle Doheny, who purchased it at auction in 1945 and donated it to the Vincentian community shortly after.</p>
<p class="para">For more information about this manuscript or the Vincentian Studies Collection, please contact Vincentian Librarian Andrew Rea at <a href="mailto:area1@depaul.edu" target="_top"> area1@depaul.edu</a>.</p>




