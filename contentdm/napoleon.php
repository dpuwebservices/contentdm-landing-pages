                        <p>Pamphlets and broadsides are ephemeral, meaning that they were originally in circulation for a brief period of time. Often printed quickly to fulfill an immediate need, and lacking the protection of bindings or covers, pamphlets and broadsides were discarded, and those copies that have persisted may be difficult to find in library catalogs, since they lack the uniform information usually located on the title page of a book. The Napoleonic Era Pamphlets and Broadsides collection may be searched by title and author, but some researchers may find it useful to narrow by language (English or French) and use keywords to navigate the collection. Many items have transcriptions included; additional transcriptions will be added as they become available.</p>
